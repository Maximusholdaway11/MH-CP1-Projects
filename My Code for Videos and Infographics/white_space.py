#Max Holdaway White Space Infographic

print("White space is what is above this line of code.")

print("It is mainly used to define functions and conditionals.")

FirstNumber = int(input("Please give me a number: "))

SecondNumber = int(input("Please give me a second number: "))

def AdditionFunction(FirstSum, SecondSum):
    z = FirstSum + SecondSum
    return z

print(AdditionFunction(FirstNumber, SecondNumber))

print("The space between the z and the numbers signifying which line you are on is white space.")